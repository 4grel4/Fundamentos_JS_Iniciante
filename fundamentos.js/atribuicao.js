const a = 7
let b = 3

b += a // esta fazendo uma adição '+=' -- b = b + a
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2 
console.log(b)

b %= 2 // b = b % 2
console.log(b) // , quando você divide um número par por 2, o resto é sempre 0. Quando você divide um número ímpar por 2, o resto é sempre 1